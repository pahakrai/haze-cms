export const email = v =>
  /^([0-9A-Za-z\-_.]+)@([0-9a-z]+\.[a-z]{2,3}(\.[a-z]{2})?)$/.test(v);

export const phone = v =>
  /^\+?(\d{0,2}-)?(0\d{1,4}-)?\d{6,9}$/.test(v) ||
  /^((\+\d{1,3}(-| )?\(?\d\)?(-| )?\d{1,5})|(\(?\d{2,6}\)?))(-| )?(\d{3,4})(-| )?(\d{4})(( x| ext)\d{1,5}){0,1}$/.test(
    v
  );
